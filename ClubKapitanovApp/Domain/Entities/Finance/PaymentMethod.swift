import Foundation

/// Способ оплаты операции.
///
/// Один заказ, штраф или продажа сувенирки должны иметь один способ оплаты. Значения
/// стандартизированы на уровне домена, чтобы в отчетах не расползались варианты вроде
/// "наличка", "перевод", "карта" в свободном тексте.
enum PaymentMethod: String, Codable, Sendable, CaseIterable {
    case cash
    case card
    case qr
}
